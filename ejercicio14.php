<?php
// Definir una variable con el nombre completo
$nombreCompleto = "Janeth Meza";

// Mostrar el número de caracteres con strlen()
echo "El número de caracteres en '$nombreCompleto' es: " . strlen($nombreCompleto) . "<br>";

// Mostrar el nombre en mayúsculas con strtoupper()
echo "El nombre en mayúsculas es: " . strtoupper($nombreCompleto) . "<br>";

// Mostrar el nombre al revés con strrev()
echo "El nombre al revés es: " . strrev($nombreCompleto) . "<br>";
?>


